def test_timeout_is_redacted_but_preserved_in_failure_log(tmp_path: Path) -> None:
    class TimeoutRunner(RecordingRunner):
        def __call__(self, *args: object, **kwargs: object) -> CompletedProcess[str]:
            if len(self.calls) == 2:
                self.calls.append({"args": list(args)})
                raise TimeoutExpired(
                    ["secret-command", "--token", "do-not-render"],
                    1,
                    output=b"timeout-secret-stdout\n",
                    stderr=b"timeout-secret-stderr\n",
                )
            return super().__call__(*args, **kwargs)

    runner = TimeoutRunner()
    evidence_dir = tmp_path / "dependency-audit"

    with pytest.raises(verifier.DependencyAuditError) as excinfo:
        verifier.verify_dependency_audit(evidence_dir, run_command=runner, now=_fixed_now)

    assert "timed out" in str(excinfo.value)
    assert "do-not-render" not in str(excinfo.value)
    assert "timeout-secret" not in str(excinfo.value)
    failure_log = (evidence_dir / "pip-audit.log").read_text(encoding="utf-8")
    assert "timeout-secret-stdout\n" in failure_log
    assert "timeout-secret-stderr\n" in failure_log
    assert not (evidence_dir / "manifest.json").exists()


