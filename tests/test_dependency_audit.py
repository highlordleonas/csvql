@pytest.mark.parametrize("output_index", [2, 3])
def test_each_audit_output_must_report_no_vulnerabilities(
    tmp_path: Path, output_index: int
) -> None:
    def add_vulnerability(call_index: int, output_path: Path) -> None:
        if call_index == output_index:
            payload = CORE_AUDIT if output_index == 2 else TUI_AUDIT
            vulnerable = json.loads(json.dumps(payload))
            vulnerable["dependencies"][0]["vulns"] = [{"id": "GHSA-test"}]
            output_path.write_text(json.dumps(vulnerable), encoding="utf-8")

    runner = RecordingRunner(mutate_output=add_vulnerability)
    evidence_dir = tmp_path / "dependency-audit"

    with pytest.raises(verifier.DependencyAuditError, match="reported vulnerabilities"):
        verifier.verify_dependency_audit(evidence_dir, run_command=runner, now=_fixed_now)

    assert len(runner.calls) == output_index + 1
    assert not (evidence_dir / "manifest.json").exists()


