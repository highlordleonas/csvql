@pytest.mark.parametrize("output_index", range(4))
def test_every_generated_output_must_be_a_regular_non_symlink_file(
    tmp_path: Path, output_index: int
) -> None:
    external = tmp_path / f"external-{output_index}"
    external.write_text("foreign", encoding="utf-8")

    def replace_with_symlink(call_index: int, output_path: Path) -> None:
        if call_index == output_index:
            output_path.unlink()
            output_path.symlink_to(external)

    runner = RecordingRunner(mutate_output=replace_with_symlink)
    evidence_dir = tmp_path / "dependency-audit"

    with pytest.raises(verifier.DependencyAuditError, match="regular non-symlink file"):
        verifier.verify_dependency_audit(evidence_dir, run_command=runner, now=_fixed_now)

    assert len(runner.calls) == output_index + 1
    assert not (evidence_dir / "manifest.json").exists()


